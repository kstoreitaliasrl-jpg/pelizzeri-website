// APPLY LANGUAGE
// ==========================================
function applyLang(lang) {
    const t = TRANSLATIONS[lang];
    if (!t) return;

    document.documentElement.lang = lang === 'it' ? 'it' : 'en';
    localStorage.setItem('lang', lang);

    // Plain text elements
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (t[key] !== undefined) el.textContent = t[key];
    });

    // HTML elements (contains tags like <em>, <br>)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.getAttribute('data-i18n-html');
        if (t[key] !== undefined) el.innerHTML = t[key];
    });

    // Input placeholders
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (t[key] !== undefined) el.placeholder = t[key];
    });

    // Typewriter data-text (hero subtitle)
    document.querySelectorAll('[data-i18n-tw]').forEach(el => {
        const key = el.getAttribute('data-i18n-tw');
        if (t[key] !== undefined) {
            el.setAttribute('data-text', t[key]);
            if (window.runTypewriter) {
                window.runTypewriter(el, t[key], 0);
            }
        }
    });

    // split-word elements that also need translation (re-split)
    document.querySelectorAll('[data-i18n-split]').forEach(el => {
        const key = el.getAttribute('data-i18n-split');
        if (t[key] !== undefined) {
            const words = t[key].split(' ');
            el.innerHTML = words.map((w, i) =>
                `<span class="word visible" style="transition-delay:${i * 0.07}s">${w}&nbsp;</span>`
            ).join('');
        }
    });

    // Update active lang button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.getAttribute('data-lang') === lang);
    });
}

function setLang(lang) {
    applyLang(lang);
}

// Apply saved or default language on load
document.addEventListener('DOMContentLoaded', () => {
    const saved = localStorage.getItem('lang') || 'it';
    applyLang(saved);
});
